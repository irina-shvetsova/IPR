@dataclass
class EmployeeIntent:
    """
    Ответы сотрудника с трёх экранов выбора.

    Атрибуты:
        full_name: ФИО сотрудника.
        role: Текущая роль.
        direction: Направление движения — 'углубление', 'горизонтально' или 'вертикально'.
        direction_note: Пояснение к выбранному направлению.
        expectations: Три главных ожидания от работы через год.
        readiness: Готовность вложиться — 'Лёгкая', 'Средняя' или 'Высокая'.
        hours_per_week: Сколько часов в неделю готов уделять ('1–2 ч', '3–5 ч', '5+ ч').
        period: Период плана (для шапки и календаря).
    """

    full_name: str
    role: str = ""
    gender: str = "женский"          # женский | мужской
    direction: str = "горизонтально"
    direction_note: str = ""
    expectations: list[str] = field(default_factory=list)
    readiness: str = "Средняя"
    hours_per_week: str = "3–5 ч"
    period: str = "Полугодие"

    @property
    def direction_label(self) -> str:
        return _DIRECTION_HINTS.get(self.direction, self.direction)

    @property
    def readiness_label(self) -> str:
        return _READINESS_HINTS.get(self.readiness, "устойчивый темп")


