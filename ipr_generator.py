@dataclass
class EmployeeIntent:
    """
    Ответы сотрудника с трёх экранов выбора.

    Атрибуты:
        full_name: ФИО сотрудника.
        role: Текущая роль.
        direction: Направление движения — 'углубление', 'горизонтально' или 'вертикально'.
        direction_note: Пояснение к выбранному направлению.
        expectations: Три ожидания от работы через год (равнозначные, без интерпретации).
        gender: Род для согласования форм. Пустое значение — определить по имени.
        readiness: Готовность вложиться — 'Лёгкая', 'Средняя' или 'Высокая'.
            Задаёт число направлений и объём действий в каждом.
        period: Период плана. В MVP всегда «Год».
    """

    full_name: str
    role: str = ""
    gender: str = ""                 # если пусто — определяется по имени
    direction: str = "горизонтально"
    direction_note: str = ""
    expectations: list[str] = field(default_factory=list)
    readiness: str = "Средняя"
    period: str = "Год"

    def __post_init__(self) -> None:
        if not self.gender:
            self.gender = infer_gender(self.full_name)

    @property
    def direction_label(self) -> str:
        return _DIRECTION_HINTS.get(self.direction, self.direction)

    @property
    def readiness_label(self) -> str:
        plan = readiness_plan(self.readiness)
        return f"{plan['hint']}, {plan['hours']}"


