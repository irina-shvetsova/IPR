def infer_gender(full_name: str) -> str:
    """
    Определяет род по имени, чтобы не спрашивать пол у пользователя.

    Берётся первое слово, похожее на имя. Русские женские имена почти всегда
    оканчиваются на «а» или «я»; известные мужские исключения перечислены явно.
    При неоднозначности возвращается мужской род как более нейтральный по умолчанию
    в деловых документах.
    """
    parts = [p for p in str(full_name).strip().split() if p]
    if not parts:
        return "мужской"
    # В «Иванова Анна» имя — второе слово; в «Анна» — первое.
    name = parts[1] if len(parts) > 1 else parts[0]
    name = name.strip(".,").lower()
    if name in _MALE_NAME_EXCEPTIONS:
        return "мужской"
    return "женский" if name.endswith(("а", "я")) else "мужской"

