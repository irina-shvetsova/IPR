def _period_label(period: str) -> str:
    """Приводит период к форме для фразы «на …»."""
    return {
        "Квартал": "ближайший квартал",
        "Полугодие": "ближайшее полугодие",
        "Год": "ближайший год",
    }.get(period, "ближайшее полугодие")
