# Порядок и названия содержательных разделов. Используются и в оглавлении,
# и в рендере документа, чтобы нумерация не разъезжалась.
SECTIONS = [
    ("section2", "Контекст развития"),
    ("section3", "Зоны роста по результатам 360°"),
    ("section4", "Направления развития"),
    ("section5", "Обучение и источники"),
    ("section7", "Точки контроля"),
    ("section8", "Согласование"),
]


def section_number(key: str) -> int:
    """Возвращает номер содержательного раздела по его ключу (1-based)."""
    for idx, (section_key, _) in enumerate(SECTIONS, start=1):
        if section_key == key:
            return idx
    return 0


def section_title(key: str) -> str:
    """Возвращает «N. Название» для содержательного раздела."""
    for idx, (section_key, name) in enumerate(SECTIONS, start=1):
        if section_key == key:
            return f"{idx}. {name}"
    return ""


